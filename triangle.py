
def area(a, h):
    """
    На вход поступают числа a и h, возвращает a умноженное на половину h
    Пример:
        Ввод:
        a=5
        h=4
        Вывод:
        area=10
    """
    return a * h / 2 

def perimeter(a, b, c):
    """
    На вход поступают числа a, b и c, возвращает сумму этих чисел
    Пример:
        Ввод:
        a=2
        b=3
        c=4
        Вывод:
        perimeter=9
    """
    return a + b + c 
