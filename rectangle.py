def area(a, b):
    """
    На вход поступают числа a и b, возвращает произведение a и b
    Пример:
        Ввод:
        a=2
        b=5
        Вывод:
        area=10
    """
    return a * b 

def perimeter(a, b):
    """
    На вход поступают числа a и b, возвращает удвоенное произведение a и b
    Пример:
        Ввод:
        a=5
        b=7
        Вывод:
        perimeter=24
    """
    return 2*(a + b) 
