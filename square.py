
def area(a):
    """
    На вход поступает число a, возвращает a в квадрате
    Пример:
        Ввод:
        a=7
        Вывод:
        area=49
    """
    return a * a


def perimeter(a):
    """
    На вход поступает число a, возвращает a умноженное на 4
    Пример:
        Ввод:
        a=2
        Вывод:
        perimeter=8
    """
    return 4 * a
